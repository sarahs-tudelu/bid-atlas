const NAME_BY_CODE = new Map(US_STATES.map(([code, name]) => [code, name]));

/** Falls back to the raw value so a state the backend adds later still renders. */
export function stateName(code: string): string {
  return NAME_BY_CODE.get(code.toUpperCase()) ?? code;
}
